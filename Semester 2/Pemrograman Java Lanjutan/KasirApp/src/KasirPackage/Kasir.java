    private void UangBayarFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_UangBayarFieldKeyTyped

        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9'){
            UangBayarField.setEditable(true);
        } else {
            UangBayarField.setEditable(false);
        }
    }//GEN-LAST:event_UangBayarFieldKeyTyped

    private void BayarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarButtonActionPerformed
        int totalUang2;
        if (UangBayarField.getText().isEmpty()) {
            totalUang2 = 0;
        } else {
            totalUang2 = Integer.valueOf(UangBayarField.getText().replaceAll("\\.", ""));
        }
        int kembalian =  totalUang2 - jumlahBelanja2;
        GetRupiah rupiah = new GetRupiah();
        rupiah.Rupiah(kembalian);
        KembalianField.setText(rupiah.harga);
    }//GEN-LAST:event_BayarButtonActionPerformed

