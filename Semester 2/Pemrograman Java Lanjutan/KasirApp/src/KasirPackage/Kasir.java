    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        if (NamaBarangBox.getSelectedItem() == null || JumlahBeliField.getText().isEmpty() || JumlahBeliField.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Pilih Nama Barang & Jumlah Beli Terlebih Dahulu!");
        } else {
            jumlahBeli = Integer.valueOf(JumlahBeliField.getText());
            totalHarga = this.hargaBarang * this.jumlahBeli;

            GetRupiah cariRupiah = new GetRupiah();

            Object[] row = {this.idBarang, this.namaBarang, this.hargaBarang, this.jumlahBeli, this.totalHarga};

            DefaultTableModel model = (DefaultTableModel) TabelPembelian.getModel();
            model.addRow(row);
            jumlahkanBelanja();
        }
    }//GEN-LAST:event_buttonTambahActionPerformed

