    void uangBayar() {
        GetRupiah rupiah = new GetRupiah();
        String coba;
        if (UangBayarField.getText().isEmpty()) {
            rupiah.Rupiah(uangBayar);
            UangBayarField.setText(rupiah.harga);
        } else {
            coba = UangBayarField.getText().replaceAll("\\.", "");
            int totalUang = Integer.valueOf(coba);
            totalUang += uangBayar;
            rupiah.Rupiah(totalUang);
            UangBayarField.setText(rupiah.harga);
        }
    }
    
    void jumlahkanBelanja() {
        DefaultTableModel model = (DefaultTableModel) TabelPembelian.getModel();
        int baris = model.getRowCount();
        int jumlah;
        int jumlahBelanja = 0;
        
        for (int i = 0; i < baris; i++) {
            jumlah = (int) model.getValueAt(i, 4);
            jumlahBelanja += jumlah;
        }
        
        jumlahBelanja2 = jumlahBelanja;
        GetRupiah cariRupiah = new GetRupiah();
        cariRupiah.Rupiah(jumlahBelanja);
        TotalHarga.setText(cariRupiah.harga);
    }

    public void TampilkanBarang() {
        try {
            sql = "select * from barang";
            rs = state.executeQuery(sql);

            while (rs.next()) {
                NamaBarangBox.addItem(rs.getString("nama_barang"));
            }
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());
        }
    }

    public void TampilkanIdDanHarga() {
        try {
            String sql1 = "select id_barang, harga_barang from barang where nama_barang='" + NamaBarangBox.getSelectedItem() + "'";
            rs = state.executeQuery(sql1);
            if (NamaBarangBox.getSelectedIndex() == 0) {
                HargaBarangField.setText("");
            } else {
                while (rs.next()) {
                    //String[] objek = new String[2];
                    idBarang = rs.getString("id_barang");
                    hargaBarang = Integer.valueOf(rs.getString("harga_barang"));
                    namaBarang = (String) NamaBarangBox.getSelectedItem();

                    GetRupiah cariRupiah = new GetRupiah();
                    cariRupiah.Rupiah(hargaBarang);

                    IDBarangField.setText(idBarang);
                    HargaBarangField.setText(cariRupiah.harga);
                }
                rs.close();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }

