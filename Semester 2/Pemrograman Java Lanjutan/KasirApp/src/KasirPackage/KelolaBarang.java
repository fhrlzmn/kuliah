    private void TBSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TBSimpanActionPerformed
        try {
            String id = TambahID.getText();
            String nama = TambahNama.getText();
            String harga = TambahHarga.getText();

            if ((id=="") && (nama=="") && (harga=="")){
                JOptionPane.showMessageDialog(null, "Mohon Masukkan Data!!!");
            } else {
                rs = state.executeQuery("select * from barang");
                if (rs.next()) {
                    if(id.equals(rs.getString("id_barang"))){
                        JOptionPane.showMessageDialog(null, "ID Tidak boleh sama!");
                    } else {
                        sql = "insert into barang values('" + id + "','" + nama + "','" + harga + "')";
                        state.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Sukses");
                        showData();
                        TambahID.setText("");
                        TambahNama.setText("");
                        TambahHarga.setText("");
                    }
                } else {
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_TBSimpanActionPerformed

