    void showData() {
        try {
            sql = "select * from barang";
            rs = state.executeQuery(sql);
            DefaultTableModel tabel = (DefaultTableModel) TabelBarang.getModel();
            tabel.setRowCount(0);
            while (rs.next()) {
                Object objek[] = {rs.getString("id_barang"), rs.getString("nama_barang"), rs.getInt("harga_barang")};
                tabel.addRow(objek);
            }
            sorter = new TableRowSorter<>(tabel);
            TabelBarang.setRowSorter(getRowSorter());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    private TableRowSorter getRowSorter() {
        DefaultTableModel tabel = (DefaultTableModel) TabelBarang.getModel();
        if(sorter == null){
            sorter = new TableRowSorter(tabel);
        }
        return sorter;
    }
    
    

