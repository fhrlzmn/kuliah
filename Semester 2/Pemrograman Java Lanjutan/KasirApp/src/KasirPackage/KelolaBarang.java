    private void HapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusButtonActionPerformed
        int row = TabelBarang.getSelectedRow();
        String nama = (String) TabelBarang.getModel().getValueAt(row, 1);
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda akan menghapus " + nama + "?" , "Konfirmasi", JOptionPane.OK_CANCEL_OPTION);
        if (confirm == 0) {
            try {
                row = TabelBarang.getSelectedRow();

                if (row > -1) {
                    String getID = (String) TabelBarang.getModel().getValueAt(row, 0);
                    String getNama = (String) TabelBarang.getModel().getValueAt(row, 1);
                    sql = "delete from barang where barang.id_barang = '" + getID + "'";
                    state.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, getNama + " Sukses dihapus!");
                    showData();
                } else {
                    JOptionPane.showMessageDialog(null, "Pilih data dalam tabel terlebih dahulu!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            // tak ada
        }       
    }//GEN-LAST:event_HapusButtonActionPerformed

