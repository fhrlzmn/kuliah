    private void RePasswordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_RePasswordFieldKeyReleased
        String RegisterPassword = new String(PasswordField.getPassword());
        String ReTypePassword = new String(RePasswordField.getPassword());
        
        if (RegisterPassword.equals(ReTypePassword)) {
            Match.setText("Password Match");
        } else if (!RegisterPassword.equals(ReTypePassword)) {
            Match.setText("Password Doesn't Match");
        }
    }//GEN-LAST:event_RePasswordFieldKeyReleased

    private void ShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowPasswordActionPerformed
        if (ShowPassword.isSelected()){
            PasswordField.setEchoChar((char)0);
            RePasswordField.setEchoChar((char)0);
        } else {
            PasswordField.setEchoChar('*');
            RePasswordField.setEchoChar('*');
        }
    }//GEN-LAST:event_ShowPasswordActionPerformed

