    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        String NamaRegister = NamaField.getText();
        String EmailRegister = EmailField.getText();
        String UsernameRegister = UsernameField.getText();
        String PasswordRegister = String.valueOf(PasswordField.getPassword());
        try {
            sql = "INSERT INTO accounts VALUES ('"+NamaRegister+"','"+EmailRegister+"','"+UsernameRegister+"','"+PasswordRegister+"')";
            state.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Registrasi Sukses");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_RegisterButtonActionPerformed

